export default function AdminLayout({ children }: { children: React.ReactNode }) {
  const { user, isLoading, logout } = useAuth();
  const router = useRouter();
  const pathname = usePathname();

  useEffect(() => {
    if (!isLoading && !user && pathname !== '/admin/login') {
      router.push('/admin/login');
    }
  }, [user, isLoading, router, pathname]);

  if (pathname === '/admin/login') {
    return <>{children}</>;
  }

  if (isLoading) {
    return (
      <div className="min-h-screen bg-primary flex items-center justify-center pt-16">
        <div className="w-8 h-8 border-2 border-accent-blue border-t-transparent rounded-full animate-spin" />
      </div>
    );
  }

  if (!user) return null;

  return (
    <div className="flex min-h-screen pt-16 bg-primary">
      {/* Sidebar */}
      <aside className="w-64 bg-primary-light border-r border-white/10 fixed left-0 top-16 bottom-0 overflow-y-auto hidden lg:block">
        <div className="p-4">
          <p className="text-xs text-text-muted uppercase tracking-wider mb-4">Admin Panel</p>
          <nav className="space-y-1">
            {adminLinks.map(link => (
              <Link key={link.href} href={link.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-colors ${
                  pathname === link.href
                    ? 'bg-accent-blue/10 text-accent-blue'
                    : 'text-text-muted hover:text-white hover:bg-white/5'
                }`}>
                <svg className="w-5 h-5 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d={link.icon} />
                </svg>
                {link.label}
              </Link>
            ))}
          </nav>
        </div>

        <div className="absolute bottom-0 left-0 right-0 p-4 border-t border-white/10">
          <div className="flex items-center justify-between">
            <div>
              <p className="text-sm text-white">{user.name}</p>
              <p className="text-xs text-text-muted">{user.role}</p>
            </div>
            <button onClick={logout} className="text-text-muted hover:text-red-400 transition-colors" title="Logout">
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
              </svg>
            </button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <div className="flex-1 lg:ml-64">
        <div className="p-6">
          {children}
        </div>
      </div>
    </div>
  );
}
